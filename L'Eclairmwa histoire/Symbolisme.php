	<p><h2>Le symbolisme</h2></p>
	 <p class="Phrase"><l1>Le symbolisme est une école artistique et littéraire, surtout poétique, qui se développe en France et en Belgique entre 1880 et 1890 et qui se termine en 1914. Il cherche à traduire, par de nouveaux moyens, une vision subjective et spiritualiste du monde. Il est né de la réaction contre le moralisme, le rationalisme et le productivisme de l'ère industrielle.<br /><l654>Exemple :</l654> à Paris, en 1904, l'exposition d'un grand modàle du "Penseur" engendra un résultat inattendu : c'est lui "l'ouvrier quelconque, anonyme, inconnu, le premier d'entre les prolétaires". Grâce aux préoccupations politiques et sociales de l'époque, il est alors adopté comme un symbole de la pensée universelle et, en 1914, un exemplaire en bronze est fondu à destination du Panthéon.</l1></p>
	
